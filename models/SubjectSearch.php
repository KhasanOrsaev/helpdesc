<?php

namespace app\models;

use Yii;
use yii\base\Model;
use yii\data\ActiveDataProvider;
use app\models\Subject;

/**
 * SubjectSearch represents the model behind the search form about `app\models\Subject`.
 */
class SubjectSearch extends Subject
{
    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['id', 'taken_by', 'created_by', 'level'], 'integer'],
            [['type', 'description', 'text', 'created_at', 'updated_at', 'finished_at', 'taken_at', 'comments', 'status', 'createdBy.name', 'takenBy.name'], 'safe'],
        ];
    }

    public function attributes()
    {
        return array_merge(parent::attributes(),['createdBy.name','takenBy.name']); // TODO: Change the autogenerated stub
    }

    /**
     * @inheritdoc
     */
    public function scenarios()
    {
        // bypass scenarios() implementation in the parent class
        return Model::scenarios();
    }

    /**
     * Creates data provider instance with search query applied
     *
     * @param array $params
     *
     * @return ActiveDataProvider
     */
    public function search($params)
    {
        $query = Subject::find()->joinWith(['createdBy']);

        // add conditions that should always apply here

        $dataProvider = new ActiveDataProvider([
            'query' => $query,
        ]);

        $this->load($params);

        if (!$this->validate()) {
            // uncomment the following line if you do not want to return any records when validation fails
            // $query->where('0=1');
            return $dataProvider;
        }

        // grid filtering conditions
        $query->andFilterWhere([
            'id' => $this->id,
            'created_at' => $this->created_at,
            'updated_at' => $this->updated_at,
            'finished_at' => $this->finished_at,
            'taken_at' => $this->taken_at,
            'taken_by' => $this->taken_by,
            'created_by' => $this->created_by,
            'level' => $this->level,
        ]);
        $query->andFilterWhere(['like', 'type', $this->type])
            ->andFilterWhere(['like', 'text', $this->text])
            ->andFilterWhere(['!=', 'status', 'C'])
            ->andFilterWhere(['like', 'comments', $this->comments])
            ->andFilterWhere(['like', 'status', $this->status])
            ->andFilterWhere(['like', 'description', $this->description])
            ->andFilterWhere(['like','display_name',$this->getAttribute('createdBy.name')])
            ->andFilterWhere(['=','org',Yii::$app->user->identity->org])
            ->andFilterWhere(['like','display_name',$this->getAttribute('takenBy.name')])
            ->orderBy(['id'=>SORT_DESC]);

        return $dataProvider;
    }
}
